    private void doiMatKhau(){
        String matKhau = new String(txtMatKhauCu.getPassword());
        String matKhauMoi = new String(txtMatKhauMoi.getPassword());
        String matKhauMoi2 = new String(txtNhapLaiMatKhauMoi.getPassword());
        if(!matKhau.equals(Auth.user.getMatKhau())){
            Msgbox.alert(this, "Nhập sai mật khẩu!");
            txtMatKhauCu.requestFocus();
        }
        else if(!matKhauMoi.equalsIgnoreCase(matKhauMoi2)){
            Msgbox.alert(this, "Xác nhận mật khẩu không đúng!");
            txtNhapLaiMatKhauMoi.requestFocus();
        }
        else{
            Auth.user.setMatKhau(matKhauMoi);
            dao.update(Auth.user);
            Msgbox.alert(this, "Đổi mật khẩu thành công");
        }
    }
    
    private boolean checkForm(){
        if (new String(txtMatKhauCu.getPassword()).trim().equals("")) {
            Msgbox.alert(this, "Mật khẩu cũ không dược để trống");
            txtMatKhauCu.requestFocus();
            return false;
        }
        else if (new String(txtMatKhauMoi.getPassword()).trim().equals("")) {
             Msgbox.alert(this, "Mật khẩu mới không dược để trống");
            txtMatKhauMoi.requestFocus();
            return false;
        }
        else if (new String(txtNhapLaiMatKhauMoi.getPassword()).trim().equals("")) {
             Msgbox.alert(this, "Nhập lại mật khẩu mới không dược để trống");
            txtMatKhauCu.requestFocus();
            return false;
        }
        return true;
    }
